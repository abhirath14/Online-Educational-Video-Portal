	var $window = $(window);
	var velocity = 0.5;

	function update(){
	    var pos = $window.scrollTop();

	    $('.test').each(function() {
	        var $element = $(this);
	        var height = $element.height();

	        $(this).css('backgroundPosition', '10% ' + Math.round((height - pos) * velocity) + 'px');
